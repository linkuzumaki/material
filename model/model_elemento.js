var mongoose = require('mongoose');
var Schema = mongoose.Schema;
var Elemento = new Schema({
    "idform": {
        "type": "string",
        "required": true
    },
    "nameform": {
        "type": "string",
        "required": true
    },
    "elements": {
        "idelement": {
            "type": "string",
            "required": true
        },
        "element": {
            "type": "string",
            "required": true
        }
    }
})

module.exports = mongoose.model('Elemento', Elemento);
